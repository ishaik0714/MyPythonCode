""" Multi line comments can be written using triple double quotes.
Start and end with triple quotes. """

print("Hello World")        # Print a string using double quotes
print('Hello World')        # Print a string using single quotes
print("My first program is \"Hello World\"")    # Print a string using escape character, Use \"

