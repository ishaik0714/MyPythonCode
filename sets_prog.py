# Sets program in Python

# Initialize the elements into the Sets.
# Sets are unordered and unindexed.
myset = {"Red","Blue","Violet","Green","Yellow","Orange"}
print(myset)            # Printing whole sets.

myset.add("White")      # Add element into sets
print(myset)            # Printing whole sets.
myset.remove("Green")     # Remove element "Green" from sets
print(myset)            # Printing whole sets.
myset.discard("Red")    # Remove element "Red" from sets
print(myset)            # Printing whole sets.
myset.pop()             # Remove element randomly from sets
print(myset)            # Printing whole sets.

myset.clear()           # Delete the whole sets.
print(myset)            # Printing whole sets.